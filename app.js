let number = 10; // Number I am going to test

// If statement checks if the remainder of "number" (10) divided by 2 is 0.
//If true it will return "The number is even."
//Else it will return "The number is odd."  
if (number % 2 === 0) {
    console.log("The number is even.");
} else {
    console.log("The number is odd.");
}